#!/usr/bin/env node
/**
 * Copy Resend credentials from mail_settings (main DB) into env files for Railway / local parity.
 * Never prints API keys to stdout unless --stdout (use only in a private terminal).
 *
 *   npm run export:system-email-from-mail
 *   npm run export:system-email-from-mail -- --write-local
 *   npm run export:system-email-from-mail -- --write-railway-file
 */
const fs = require('fs');
const path = require('path');
const { Pool } = require('pg');

require('dotenv').config({ path: path.join(__dirname, '..', '.env') });
require('dotenv').config({ path: path.join(__dirname, '..', '.env.local'), override: true });

const root = path.join(__dirname, '..');
const writeLocal = process.argv.includes('--write-local');
const writeRailwayFile = process.argv.includes('--write-railway-file');
const toStdout = process.argv.includes('--stdout');

const dbUrl =
  process.env.DATABASE_URL ||
  process.env.PROD_MAIN_DATABASE_URL ||
  process.env.RAILWAY_DATABASE_URL;

const railwayUrl =
  process.env.PROD_APP_URL ||
  process.env.RAILWAY_APP_URL ||
  'https://sweet-courtesy-production-fa4e.up.railway.app';

async function loadFromMail() {
  if (!dbUrl) {
    throw new Error('DATABASE_URL or PROD_MAIN_DATABASE_URL is required');
  }

  const pool = new Pool({ connectionString: dbUrl });
  try {
    const hasTable = await pool.query(
      `SELECT 1 FROM information_schema.tables
       WHERE table_schema = 'public' AND table_name = 'mail_settings'`,
    );
    if (!hasTable.rows.length) {
      throw new Error('mail_settings table not found — configure Mail → Resend first');
    }

    const r = await pool.query(
      `SELECT user_id, provider, resend_api_key, resend_from_address, from_address
       FROM mail_settings
       WHERE provider = 'resend' AND resend_api_key IS NOT NULL AND TRIM(resend_api_key) <> ''
       ORDER BY user_id
       LIMIT 1`,
    );
    const row = r.rows[0];
    if (!row) {
      throw new Error('No Resend API key in mail_settings — save Mail → Settings → Resend first');
    }

    const from =
      (row.resend_from_address && String(row.resend_from_address).trim()) ||
      (row.from_address && String(row.from_address).trim()) ||
      '';

    if (!from) {
      throw new Error('resend_from_address missing in mail_settings');
    }

    return {
      userId: row.user_id,
      resendApiKey: String(row.resend_api_key).trim(),
      resendFrom: from,
    };
  } finally {
    await pool.end();
  }
}

function upsertEnvLines(filePath, lines) {
  const keys = new Set(lines.map((l) => l.split('=')[0]));
  let body = '';
  if (fs.existsSync(filePath)) {
    body = fs.readFileSync(filePath, 'utf8');
    body = body
      .split('\n')
      .filter((line) => {
        const k = line.split('=')[0]?.trim();
        return !keys.has(k);
      })
      .join('\n')
      .replace(/\n+$/, '');
  }
  const block = `\n# System email (from mail_settings via export-system-email-from-mail.js)\n${lines.join('\n')}\n`;
  fs.writeFileSync(filePath, (body ? `${body}\n` : '') + block, 'utf8');
}

async function main() {
  const { userId, resendApiKey, resendFrom } = await loadFromMail();
  const base = railwayUrl.replace(/\/+$/, '');

  const envLines = [
    `RESEND_API_KEY=${resendApiKey}`,
    `RESEND_FROM=${resendFrom}`,
    `FRONTEND_URL=${base}`,
    `APP_URL=${base}`,
  ];

  console.log(`Source: mail_settings user_id=${userId}`);
  console.log(`RESEND_FROM=${resendFrom}`);
  console.log(`FRONTEND_URL=${base}`);

  if (writeLocal) {
    const localPath = path.join(root, '.env.local');
    upsertEnvLines(localPath, [
      `RESEND_API_KEY=${resendApiKey}`,
      `RESEND_FROM=${resendFrom}`,
      `FRONTEND_URL=http://localhost:3001`,
    ]);
    console.log(`\n✅ Updated ${localPath} (FRONTEND_URL=http://localhost:3001 for dev)`);
  }

  if (writeRailwayFile) {
    const outPath = path.join(root, '.env.railway.resend');
    const content = [
      '# Paste these into Railway → Variables (Homebase service), then redeploy.',
      '# Generated by npm run export:system-email-from-mail -- --write-railway-file',
      '# Do not commit — file is gitignored.',
      '',
      ...envLines,
      '',
    ].join('\n');
    fs.writeFileSync(outPath, content, 'utf8');
    console.log(`\n✅ Wrote ${outPath} — copy values into Railway Variables`);
  }

  if (toStdout) {
    console.log('\n---');
    for (const line of envLines) {
      console.log(line);
    }
  }

  if (!writeLocal && !writeRailwayFile && !toStdout) {
    console.log('\nNext:');
    console.log('  npm run export:system-email-from-mail -- --write-railway-file');
    console.log('  npm run export:system-email-from-mail -- --write-local');
  }
}

main().catch((err) => {
  console.error(err.message || err);
  process.exit(1);
});
