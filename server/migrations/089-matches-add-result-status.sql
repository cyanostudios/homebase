-- 089: Add result, score, competition and status fields to matches

ALTER TABLE matches
  ADD COLUMN IF NOT EXISTS home_score SMALLINT,
  ADD COLUMN IF NOT EXISTS away_score SMALLINT,
  ADD COLUMN IF NOT EXISTS result VARCHAR(50),
  ADD COLUMN IF NOT EXISTS competition_name VARCHAR(255),
  ADD COLUMN IF NOT EXISTS is_canceled BOOLEAN NOT NULL DEFAULT FALSE,
  ADD COLUMN IF NOT EXISTS is_finished BOOLEAN NOT NULL DEFAULT FALSE,
  ADD COLUMN IF NOT EXISTS is_postponed BOOLEAN NOT NULL DEFAULT FALSE;
